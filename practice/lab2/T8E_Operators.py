# Python Operators

'''
Python divides the operators in the following groups:

 * Arithmetic operators
 * Assignment operators
 * Comparison operators
 * Logical operators
 * Identity operators
 * Membership operators
 * Bitwise operators

'''

#1 Python Arithmetic Operators
'''
+	Addition	    x + y	
-	Subtraction	    x - y	
*	Multiplication	x * y	
/	Division	    x / y	
%	Modulus	        x % y
**	Exponentiation	x ** y	
//	Floor division  x // y | returns the smaller number obtained as a result of division
'''

#2 Python Assignment Operators
'''     example     Same as
+=	    x += 3	   x = x + 3
-=	    x -= 3     x = x - 3

# we can do the same with all assignment operations
'''

#3 Python Comparison Operators
'''
==	Equal       	            x == y	
!=	Not equal	                x != y	
>	Greater than	            x > y	
<	Less than	                x < y	
>=	Greater than or equal to	x >= y	
<=	Less than or equal to	    x <= y
'''

#4 Python Logical Operators
'''
and		x < 5 and  x < 10	
or		x < 5 or x < 4	
not		not(x < 5 and x < 10)
and - True value if both comparisons has a True value
or - True value, if one of two comparisons has a True value
not - True value, if it's False, and vice versa
'''

#5 Python Identity Operators
'''
Identity operators are used to compare the objects, not if they are equal,
but if they are actually the same object, with the same memory location:

is 	        x is y	
is not		x is not y
'''

#6 Python Membership Operators
'''
Membership operators are used to test if a sequence is presented in an object:

in 	        x in y	
not in	    x not in y
'''

#7 Python Bitwise Operators
'''
& 	AND		x & y	
|	OR		x | y	
^	XOR	 	x ^ y	
~	NOT		~x	
<<	Zero fill left shift	Shift left by pushing zeros in from the right and let the leftmost bits fall off	                    x << 2	
>>	Signed right shift	Shift right by pushing copies of the leftmost bit in from the left, and let the rightmost bits fall off	    x >> 2
'''
